#include <unistd.h>

void	ft_div_mod(int a, int b, int *div, int *mod)
{
	if (*b != '0')
	{
		*div = a / b;
		*mod = a % b;
	}
	else
	{
		*mod = 0;
		*div = 0;
	}
}
